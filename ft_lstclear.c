#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !del || !*lst)
		return ;
	while (*lst)
	{
		tmp = *lst;
		(*del)((*lst)->content);
		free(*lst);
		*lst = NULL;
		*lst = tmp->next;
	}
}
