#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	const char	*str;

	str = s + ft_strlen(s);
	while (1)
	{
		if (*str == (char)c)
			return ((char *)str);
		if (str == s)
			return (NULL);
		str--;
	}
	return (NULL);
}
