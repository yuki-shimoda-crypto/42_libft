#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	char	*save;

	ptr = (char *)malloc(ft_strlen(s1) + 1);
	if (!ptr)
		return (NULL);
	save = ptr;
	while (*s1)
		*ptr++ = *s1++;
	*ptr = 0;
	return (save);
}
